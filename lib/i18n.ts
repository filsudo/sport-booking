type Dictionary = (typeof dictionaries)['en']

type Primitive = string | number | boolean | null | undefined

function resolvePath(input: unknown, path: string): unknown {
  if (!path) return input
  return path.split('.').reduce<unknown>((acc, key) => {
    if (!acc || typeof acc !== 'object') return undefined
    return (acc as Record<string, unknown>)[key]
  }, input)
}

function applyVars(template: string, vars?: Record<string, Primitive>) {
  if (!vars) return template
  return template.replace(/\{(\w+)\}/g, (_, key: string) => String(vars[key] ?? `{${key}}`))
}

export function normalizeLang(input: string | null | undefined): Lang | null {
  if (!input) return null
  const value = input.toLowerCase().trim()
  if (value === 'en') return 'en'
  if (value === 'sk') return 'sk'
  return null
}

export function getDateLocale(lang: Lang): Locale {
  return locales[lang]
}

export function getDictionary(lang: Lang): Dictionary {
  return dictionaries[lang] as unknown as Dictionary
}

export function t(lang: Lang, key: string, vars?: Record<string, Primitive>): string {
  const value = resolvePath(dictionaries[lang], key)
  if (typeof value === 'string') {
    return applyVars(value, vars)
  }
  const fallback = resolvePath(dictionaries.en, key)
  if (typeof fallback === 'string') {
    return applyVars(fallback, vars)
  }
  return key
}

export function tList(lang: Lang, key: string): string[] {
  const value = resolvePath(dictionaries[lang], key)
  if (Array.isArray(value) && value.every((item) => typeof item === 'string')) {
    return value as string[]
  }
  const fallback = resolvePath(dictionaries.en, key)
  if (Array.isArray(fallback) && fallback.every((item) => typeof item === 'string')) {
    return fallback as string[]
  }
  return []
}

